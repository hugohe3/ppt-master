# 适配器：Cursor
# ============================================================
def build_cursor():
    """生成 .cursor/rules/ppt-master.md — 合并为单文件"""
    print("\n[Cursor] 生成 .cursor/rules/ppt-master.md")
    refs = read_all_references()

    parts = [GENERATED_HEADER]
    parts.append("# PPT Master — AI 驱动的 SVG 内容生成系统\n\n")
    parts.append("> 本文件由 `build_adapters.py` 从 `skills/ppt-master/` 自动生成。\n\n")

    # SKILL.md 的核心流程部分
    skill = read_skill_md()
    skill = adapt_paths_for_root(skill)
    # 去掉 YAML frontmatter
    if skill.startswith("---"):
        end = skill.find("---", 3)
        if end != -1:
            skill = skill[end + 3:].strip()
    parts.append(skill)
    parts.append("\n\n---\n\n")

    # 按顺序合并 references
    ref_order = [
        "shared-standards.md",
        "canvas-formats.md",
        "design-guidelines.md",
        "strategist.md",
        "image-generator.md",
        "executor-base.md",
        "executor-general.md",
        "executor-consultant.md",
        "executor-consultant-top.md",
        "optimizer-crap.md",
        "template-designer.md",
        "image-layout-spec.md",
        "svg-image-embedding.md",
    ]

    for name in ref_order:
        if name in refs:
            content = refs[name]
            content = adapt_paths_for_root(content)
            # 去掉引用其他 reference 的行（在合并文件中不需要）
            lines = content.split("\n")
            lines = [l for l in lines if not l.startswith("> 📎")]
            content = "\n".join(lines)
            parts.append(f"\n---\n\n")
            parts.append(content)

    write_file(ROOT / ".cursor" / "rules" / "ppt-master.md", "".join(parts))


