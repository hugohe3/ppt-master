import shutil
from pathlib import Path
from datetime import datetime

# 项目根目录
ROOT = Path(__file__).parent
SKILL_DIR = ROOT / "skills" / "ppt-master"
REFS_DIR = SKILL_DIR / "references"

GENERATED_HEADER = (
    "<!-- AUTO-GENERATED by build_adapters.py — DO NOT EDIT MANUALLY -->\n"
    f"<!-- Source: skills/ppt-master/ | Generated: {datetime.now().strftime('%Y-%m-%d')} -->\n\n"
)


def read_file(path: Path) -> str:
    """读取文件内容"""
    return path.read_text(encoding="utf-8")


def write_file(path: Path, content: str):
    """写入文件，自动创建目录"""
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    print(f"  [OK] {path.relative_to(ROOT)}")


def read_skill_md() -> str:
    """读取 SKILL.md"""
    return read_file(SKILL_DIR / "SKILL.md")


def read_all_references() -> dict[str, str]:
    """读取所有 reference 文件，返回 {文件名: 内容}"""
    refs = {}
    for f in sorted(REFS_DIR.glob("*.md")):
        refs[f.name] = read_file(f)
    return refs


def adapt_paths_for_root(content: str) -> str:
    """将 skill 内部路径适配为仓库根目录视角"""
    replacements = {
        "${SKILL_DIR}/scripts/": "skills/ppt-master/scripts/",
        "${SKILL_DIR}/templates/": "skills/ppt-master/templates/",
        "${SKILL_DIR}/": "skills/ppt-master/",
        "references/": "skills/ppt-master/references/",
    }
    for old, new in replacements.items():
        content = content.replace(old, new)
    return content


def adapt_paths_for_claude(content: str) -> str:
    """将 skill 内部路径适配为 .claude/skills/ 视角"""
    # Claude Code 的 skill 路径是相对于 .claude/skills/ppt-master/
    # 脚本和模板在 skills/ppt-master/ 下，需要用 ../../../skills/ppt-master/
    replacements = {
        "${SKILL_DIR}/scripts/": "../../../skills/ppt-master/scripts/",
        "${SKILL_DIR}/templates/": "../../../skills/ppt-master/templates/",
        "${SKILL_DIR}/": "../../../skills/ppt-master/",
    }
    for old, new in replacements.items():
        content = content.replace(old, new)
    return content


# ============================================================
# 适配器：Claude Code
# ============================================================
def build_claude():
    """生成 .claude/skills/ppt-master/"""
    print("\n[Claude Code] 生成 .claude/skills/ppt-master/")
    out_dir = ROOT / ".claude" / "skills" / "ppt-master"

    # 复制 SKILL.md（路径适配）
    skill_content = read_skill_md()
    skill_content = adapt_paths_for_claude(skill_content)
    write_file(out_dir / "SKILL.md", GENERATED_HEADER + skill_content)

    # 复制 references/
    refs_out = out_dir / "references"
    for f in sorted(REFS_DIR.glob("*.md")):
        content = read_file(f)
        write_file(refs_out / f.name, content)


