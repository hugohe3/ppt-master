ADAPTERS = {
    "claude": build_claude,
    "cursor": build_cursor,
    "windsurf": build_windsurf,
    "agents": build_agents,
    "claude-md": build_claude_md,
    "gemini": build_gemini,
    "agent-sdk": build_agent_sdk,
}


def build_all():
    """生成所有适配文件"""
    print(f"PPT Master — 多平台适配文件生成器")
    print(f"规范源: {SKILL_DIR.relative_to(ROOT)}/")
    print(f"生成时间: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")

    for name, builder in ADAPTERS.items():
        builder()

    print(f"\n[DONE] 全部完成！共生成 {len(ADAPTERS)} 个适配。")


def main():
    if len(sys.argv) < 2:
        build_all()
    else:
        target = sys.argv[1].lower()
        if target == "all":
            build_all()
        elif target in ADAPTERS:
            ADAPTERS[target]()
            print(f"\n[DONE] {target} 适配生成完成。")
        else:
            print(f"未知目标: {target}")
            print(f"可用目标: {', '.join(ADAPTERS.keys())}, all")
            sys.exit(1)


if __name__ == "__main__":
    main()
